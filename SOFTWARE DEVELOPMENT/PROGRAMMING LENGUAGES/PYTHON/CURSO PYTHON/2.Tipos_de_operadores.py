# 4) Operadores lógicos
# AND El operador and genera como resultado el valor True, solamente cuando son ciertos sus dos operandos.
# OR  El operador or da como resultado el valor True, si cualquiera de sus operandos es True, y False sólo cuando ambos operandos son False.
# NOT El operador not es unario, y proporciona el valor True si su operando es False y viceversa.

# Si se desea verificar si un elemento es True o False, se puede convertir a su valor booleano mediante la función bool().
